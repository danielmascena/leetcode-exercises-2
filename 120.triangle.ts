// @lc code=start
function minimumTotal(triangle: number[][]): number {
  const { min } = Math;
  const LEN = triangle.length;
  let ans = Number.MAX_SAFE_INTEGER;
  const dp = (y: number, x: number, t = 0, p = "") => {
    if (t > 0 && t > ans) {
      return;
    }
    if (y === LEN) {
      ans = min(ans, t);
      return;
    }
    const v = triangle[y]?.[x]!;
    t += v;
    p += "," + v;
    dp(++y, x, t, p);

    if (x + 1 < (triangle[y]?.length ?? 0)) {
      dp(y, x + 1, t, p);
    }
  };
  dp(0, 0);
  return ans;
}
